function capitalizeFirstLetter(string) {
    return string.charAt(0).toUpperCase() + string.slice(1);
}

function charLimit (str, limit) {
	var reducedStr = str;
	if(reducedStr.length > limit) {
		reducedStr = reducedStr.substring(0, limit-3) + '...';
	}
	return reducedStr;
}


